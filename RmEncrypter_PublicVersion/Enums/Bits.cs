namespace RmEncrypter_PublicVersion
{
    /// <summary>
    /// Defines a size in bits.
    /// </summary>
    public enum Bits
    {
        /// <summary>
        /// Indicates 8-bit size.
        /// </summary>
        _8 = 8,

        /// <summary>
        /// Indicates 16-bit size.
        /// </summary>
        _16 = 16,

        /// <summary>
        /// Indicates 32-bit size.
        /// </summary>
        _32 = 32,

        /// <summary>
        /// Indicates 64-bit size.
        /// </summary>
        _64 = 64,

        /// <summary>
        /// Indicates 128-bit size.
        /// </summary>
        _128 = 128,

        /// <summary>
        /// Indicates 256-bit size.
        /// </summary>
        _256 = 256,

        /// <summary>
        /// Indicates 512-bit size.
        /// </summary>
        _512 = 512,

        /// <summary>
        /// Indicates 1024-bit size.
        /// </summary>
        _1024 = 1024,

        /// <summary>
        /// Indicates 2048-bit size.
        /// </summary>
        _2048 = 2048,

        /// <summary>
        /// Indicates 4096-bit size.
        /// </summary>
        _4096 = 4096,
    }
}
